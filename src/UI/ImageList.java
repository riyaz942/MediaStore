    public class ImagesTableModel extends AbstractTableModel {

    private ArrayList<InfoHolder> holder;
    private String columns[];
    
    public ImagesTableModel(ArrayList<InfoHolder> holder,String columns[]) {
        this.holder=holder;
        this.columns=columns;
    }

    public int getColumnCount() {
        return columns.length;
    }

    public int getRowCount() {
        return holder.size();
    }

    public String getColumnName(int columnIndex) {      
        return columns[columnIndex];
    }

    public Object getValueAt(int rowIndex, int columnIndex) {
        InfoHolder ah = holder.get(rowIndex);

        switch (columnIndex) {
        case 0: return ah.Path;
        case 1: return ah.File_Name ;
        case 2: return ah.Folder_Name;
        case 3: return ah.Path;
        }
        
        return null;
    }
}
        
    
    class PlusMinusCellRenderer extends JPanel implements TableCellRenderer {
        public Component getTableCellRendererComponent(
                            final JTable table, Object value,
                            boolean isSelected, boolean hasFocus,
                            int row, int column) {
               
            
            switch (column) {
                case 0:
                    File file = new File(value.toString());
                    
                    if(file.exists()){
                    JLabel label = new JLabel();
                    StretchIcon icon = new StretchIcon(file.getPath());
                    label.setIcon(icon);
                    label.setPreferredSize(new Dimension(40,40));
                
                    this.add(label);
                    }
                  break;
                default:
                   
                    JLabel label;
                    if(value!=null)
                    label= new JLabel(value.toString());
                    else
                        label = new JLabel();
                     
                    this.add( label );
                    break;
            }
                
                return this;
        }
}
    
    
    private void print() {
    Document document = new Document(PageSize.A4.rotate());
    try {
      PdfWriter writer = PdfWriter.getInstance(document, new FileOutputStream("jTable.pdf"));

      document.open();
      PdfContentByte cb = writer.getDirectContent();

      cb.saveState();
      Graphics2D g2 = cb.createGraphicsShapes(500, 500);

      Shape oldClip = g2.getClip();
      g2.clipRect(0, 0, 500, 500);

      jTable1.print(g2);
      g2.setClip(oldClip);

      g2.dispose();
      cb.restoreState();
    } catch (Exception e) {
      System.err.println(e.getMessage());
    }
    document.close();
  }

